(function () {
  var lang = localStorage.getItem("visus-lang") || (navigator.language || "en").slice(0, 2);
  if (lang !== "pt" && lang !== "en") lang = "en";

  function apply(l) {
    document.documentElement.lang = l === "pt" ? "pt-PT" : "en";
    document.querySelectorAll("[data-i18n]").forEach(function (el) {
      var k = el.getAttribute("data-i18n");
      if (T[l] && T[l][k]) el.innerHTML = T[l][k];
    });
    document.querySelectorAll(".lang a").forEach(function (a) {
      a.classList.toggle("active", a.getAttribute("data-lang") === l);
    });
    localStorage.setItem("visus-lang", l);
  }

  document.addEventListener("DOMContentLoaded", function () {
    apply(lang);
    document.querySelectorAll(".lang a").forEach(function (a) {
      a.addEventListener("click", function (e) {
        e.preventDefault();
        apply(a.getAttribute("data-lang"));
      });
    });
  });
})();
